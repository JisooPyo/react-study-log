// Chrome V8 JavaScript 엔진으로 빌드된 JavaScript 런타임.

// Node.js가 필요한 이유
//
// JavaScript는 웹 페이지 내부에 필요한 아주 단순한 기능만을 개발하기 위해 만들어졌다.
// JavaScript는 매우 유연하게 설계 되었기 때문에 생산성이 매우 높았다.
// 많은 사람들은 이 언어로 웹 페이지 내부에서만 쓰는 것을 넘어서,
// 웹 브라우저 바깥에서도 JavaScript를 사용하여 프로그램을 만들고 싶어 했다.
// 이에 2009년도에 Node.js가 개발되면서 이것(웹 서버, 모바일 앱, 데스크톱 앱 개발 등)이 가능해졌다.
